#include <stdio.h>
#include <stdlib.h>

int main()
{
    char harf1 = 'A';
    char harf2 = 67;
    char harf3 = 97;
    char harf4 = 61;

    printf ("harf1 degiskeninin karakter degeri\n %c\n",harf1);
    printf ("harf1 degiskeninin sayi degeri\n %d\n",harf1);

    printf ("harf2 degiskeninin karakter degeri\n %c\n",harf2);
    printf ("harf2 degiskeninin sayi degeri\n %d\n",harf2);

    printf ("harf3 degiskeninin karakter degeri\n %c\n",harf3);
    printf ("harf3 degiskeninin sayi degeri\n %d\n",harf3);

    printf ("harf4 degiskeninin karakter degeri\n %c\n",harf4);
    printf ("harf4 degiskeninin sayi degeri\n %i\n",harf4);
    // %d yerine %i de integer için kullanılır.
    return 0;
}
